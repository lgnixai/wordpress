<p>根据小微企业增值税相关优惠政策:增值税小规模纳税人和营业税纳税人月销售额或营业额不超过3万元(含3万元，下同)的，按照文件规定免征增值税或营业税。其中，以1个季度为纳税期限的增值税小规模纳税人和营业税纳税人，季度销售额或营业额不超过9万元的，按照文件规定免征增值税或营业税。不缴纳税款每月还是要按时零申报的。</p>
