<div class="wp-block-column"><!-- wp:paragraph -->
<p>《关于推行办税人员实名办税的公告》第二条中规定了实名的范围：</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>办税人员包括办理涉税事项的法定代表人（包括负责人、经营者，以下简称法定代表人）、财务负责人、办税员、税务代理人和经法定代表人授权的其他人员。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>如果代理公司要代你们公司办理涉税事项，是要进行实名认证的。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>《关于推行办税人员实名办税的公告》第二条中规定了实名的范围：</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>办税人员包括办理涉税事项的法定代表人（包括负责人、经营者，以下简称法定代表人）、财务负责人、办税员、税务代理人和经法定代表人授权的其他人员。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>如果代理公司要代你们公司办理涉税事项，是要进行实名认证的。</p>
