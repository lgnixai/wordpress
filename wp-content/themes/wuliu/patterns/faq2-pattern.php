<div class="wp-block-column"><!-- wp:paragraph -->
<p><strong>1.是否可提供发票？</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>可以提供发票。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>2.发票内容可以自助选择吗？</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>发票统一为旅游综合服务费的增值税普通发票，不可以自助选择。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>3.一个订单可以开具多张发票吗？</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>一般发票只开具一张，如需要开具多张发票可向客服说明情况。&nbsp;&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>4.如何获取发票？</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>在线申请发票需支付20元快递费用。旅行结束后，若需要获取发票请在2个月内可拨打我们客服电话索取，发票开好后将以到付的形式快递给您。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>索取发票需提供的信息：</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>1.个人：姓名，发票金额（不得超过实付金额），收件人，邮寄地址。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>2.单位：单位名称，统一社会信用代码（或纳税人识别号），发票金额（不得超过实付金额），单位地址、电话，收件人，邮寄地址。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>发票邮寄方式及费用：</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>发票以快递形式邮寄。报名时已填写发票信息需付20.00元快递费用，其他时段申请发票均以快递到付形式寄出。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>特别声明</strong>：</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>1.跨年度及超过2个月未索取发票的无法开具发票。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>2.代订房车费用无法开具发票。</p>
